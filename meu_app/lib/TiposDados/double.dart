void main(List<String> arguments) {
  // Variável double armazena números de ponto flutuante (números com casas decimais)
  double meuDouble = 3.14;
  print("meuDouble é: $meuDouble"); // Saída: meuDouble é: 3.14

  meuDouble = 2.71828;
  print("Agora meuDouble é: $meuDouble"); // Saída: Agora meuDouble é: 2.71828

  // Você pode realizar operações matemáticas com variáveis double
  double resultado = meuDouble * 5;
  print("Resultado: $resultado"); // Saída: Resultado: 13.5914
}
